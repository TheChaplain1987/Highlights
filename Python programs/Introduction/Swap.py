# Program to swap two variables

# Get user inputs
input1 = input('Please enter input 1: ')
input2 = input('Please enter input 2: ')

# Print pre swap inputs
print('Pre swap')
print('Input 1: ', input1)
print('Input 2: ', input2)

# Swap inputs
temp = input1
input1 = input2
input2 = temp

# Print post swap inputs
print('Post swap')
print('Input 1: ', input1)
print('Input 2: ', input2)
